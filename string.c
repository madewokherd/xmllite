static inline XMLWCHAR xml_tolower( XMLWCHAR ch )
{
    return ch + wine_casemap_lower[wine_casemap_lower[ch >> 8] + (ch & 0xff)];
}

int xml_wcsnicmp( const XMLWCHAR *str1, const XMLWCHAR *str2, int n )
{
    int ret = 0;
    for ( ; n > 0; n--, str1++, str2++)
        if ((ret = xml_tolower(*str1) - xml_tolower(*str2)) || !*str1) break;
    return ret;
}

int xml_wcsicmp( const XMLWCHAR *str1, const XMLWCHAR *str2 )
{
    for (;;)
    {
        int ret = xml_tolower(*str1) - xml_tolower(*str2);
        if (ret || !*str1) return ret;
        str1++;
        str2++;
    }
}
 
